        /// <summary>
        /// Returns the probability given the zScore.
        /// </summary>
        /// <param name="zScore">z-score with a maximum of 2 decimal places, additional decimals will be rounded.</param>
        /// <returns></returns>
        public double GetProbability(double zScore)
        {
            zScore = Math.Round(zScore, 2);
            if(tableZScore == null)
                CreateZScoreTable();
            return tableZScore[zScore];
        }

        /// <summary>
        /// Gets the zScore key given the probability value
        /// </summary>
        /// <param name="probability">must be a value between 0-1 </param>
        /// <returns>double zScore value</returns>
        public double GetZScore(double probability)
        {
            if (tableProb == null)
                CreateProbabilityKeyTable();
            int p = 0, z = 1, upper = 1200, lower = 0, i = 601;
            if (tableProb[upper, p] == probability)
                return tableProb[upper, z];
            if (tableProb[lower, p] == probability)
                return tableProb[lower, z];
            while (upper - lower > 1)
            {
                i = lower + ((upper - lower) / 2);
                if (tableProb[i, p] == probability)
                    return tableProb[i, z];
                if (tableProb[i, p] > probability)
                    upper = i;
                else lower = i;
            }
            double l = (lower - probability) * (lower - probability); //squared to ensure values are postive/comparable
            double u = (upper - probability) * (upper - probability);
            return (l > u) ? tableProb[upper, z] : tableProb[lower, z];
        }

